NUM_1 = int(input("Digite o primeiro número:"))
NUM_2 = int(input("Digite o segundo número:"))
if NUM_1 > NUM_2:
    print(f"{NUM_1} é maior que {NUM_2}")

if NUM_1 >= NUM_2:
    print(f"{NUM_1} é maior ou igual a {NUM_2}")

if NUM_1 < NUM_2:
    print(f"{NUM_1} é menor que {NUM_2}")

if NUM_1 <= NUM_2:
    print(f"{NUM_1} é menor ou igual a {NUM_2}")

if NUM_1 == NUM_2:
    print(f"{NUM_1} é igual a {NUM_2}")

if NUM_1 != NUM_2:
    print(f"{NUM_1} é diferente de {NUM_2}")
